def fizzbuzz(x):

    b = x % 3
    
    d = x % 5

    

    if ((b == 0) and (d == 0)):
        return("FizzBuzz")

    if ((b == 0) and (d != 0)):
        return("Fizz")

    else:
        return("Buzz")


    
